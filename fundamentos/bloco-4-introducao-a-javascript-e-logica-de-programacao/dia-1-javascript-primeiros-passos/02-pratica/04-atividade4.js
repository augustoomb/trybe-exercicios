/* Faça um programa que, dado um valor definido numa constante, retorne
 "positive" se esse valor for positivo, "negative" se for negativo e "zero" caso contrário.*/

 const valor = 100

 if(valor > 0){
     console.log("Número é positivo")
 }
 else if(valor < 0){
     console.log("Número é negativo")
 }
 else {
     console.log("Número é igual a zero")
 }